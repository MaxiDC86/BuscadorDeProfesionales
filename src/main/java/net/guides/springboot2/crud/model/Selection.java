package net.guides.springboot2.crud.model;

public class Selection {

	private String zone;
	private String city;
	private String area;
	private String special;

	public Selection() {
	}

	public Selection(String zone, String city, String area, String special) {
		this.zone = zone;
		this.city = city;
		this.area = area;
		this.special = special;
	}

	public String getZone() {
		return zone;
	}

	public void setZone(String zone) {
		this.zone = zone;
	}

	public String getCity1() {
		return city;
	}
	public String getCity2() {
		return city;
	}

	public String getCity3() {
		return city;
	}

	public void setCity1(String city) {
		this.city = city;
	}

	public void setCity2(String city) {
		this.city = city;
	}

	public void setCity3(String city) {
		this.city = city;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getSpecial1() {
		return special;
	}
	public String getSpecial2() {
		return special;
	}

	public void setSpecial1(String special) {
		this.special = special;
	}
	
	public void setSpecial2(String special) {
		this.special = special;
	}
	
	

}
